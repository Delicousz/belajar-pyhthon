a, b = 18, 4

print(a, '+', b, '=', a + b) # tambah
print(a, '-', b, '=', a - b) # kurang
print(a, '*', b, '=', a * b) # kali 
print(a, '/', b, '=', a / b) # bagi 
print(a, '%', b, '=', a % b) # modulus atau nilai sisa 
print(a, '**', b, '=', a ** b) # pangkat
print(a, '//', b, '=', a //b) # bagi terus dibulatkan
