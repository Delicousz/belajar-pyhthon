a, b = 13, 14

print(a, '>', b, '=', a > b) # lebih dari
print(a, '<', b, '=', a < b) # kurang dari 
print(a, '==', b, '=', a == b) # sama dengan 
print(a, '!=', b, '=', a != b) # tidak sama dengan 
print(a, '>=', b, '=', a >= b) # lebih dari sama dengan
print(a, '<=', b, '=', a <= b) # kurang dari sama dengan 
